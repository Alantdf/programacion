var numero = 123;

// Conversión de número a cadena de texto
var numerocadena = String(numero);

// Conversión de cadena de texto a número entero
var cadena = "456";
var cadenanumero = Number(cadena);

// Mostrar los resultados en la consola
console.log("Número original:", numero); 
console.log("Número como cadena de texto:", numerocadena);

console.log("Cadena original:", cadena);
console.log("Cadena convertida a número entero:", cadenanumero);
